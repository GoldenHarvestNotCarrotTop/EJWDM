package by.training.lonelyday.tasks_01.task10.entity;

public class PointEntity {
    public double x;
    public double y;

    public PointEntity(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
