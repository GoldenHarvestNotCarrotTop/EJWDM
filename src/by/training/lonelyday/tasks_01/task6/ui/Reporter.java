package by.training.lonelyday.tasks_01.task6.ui;

public class Reporter {

    public static void reportString(String s) {
        System.out.println(s);
    }



    public static void reportHours(int hours) {
        System.out.println("Number of full hours:");
        System.out.println(hours);
    }

    public static void reportMinutes(int minutes) {
        System.out.println("Number of full minutes:");
        System.out.println(minutes);
    }

    public static void reportSeconds(int seconds){
        System.out.println("Number of full seconds:");
        System.out.println(seconds);
    }
}
